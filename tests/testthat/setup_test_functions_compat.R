  PR <- c(.15, .25, .35, .45)
  CombinedDat_Aggr <- cbind.data.frame(expand.grid(ages, years, PR), scens)
  colnames(CombinedDat_Aggr)[1:3] <- c("age", "year", "PR")

  CombinedDat_Aggr <- CombinedDat_Aggr %>% dplyr::arrange(HistScen_nr, fut, age)
